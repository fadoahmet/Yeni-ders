public class forcycle {
    public static void main(String[] args) {

        //for
        
        for(int i=0;i<=10;i+=2){

            System.out.println(i); 
        }
             System.out.println("For cycle is over");
             
             //while
             int i=0;

            while(i<10) {
                System.out.println(i);
                i++;

        }

                System.out.println("While cycle is over");

        
    }
    
}
